<p>
    <strong>von Weilheim</strong> --&gt; Schongau &uuml;ber Burggen nach Bernbeuren. In Bernbeuren Richtung Auerberg. Nach dem Rehgehege auf halber H&ouml;he rechts Abzweigung G&uuml;nther-St&ouml;tten. Nach 300 m geradeaus ist Badwerk ausgeschildert. Reststrecke ca. 2 Km.
</p>
<p>
    <strong>von Marktoberdorf</strong> --&gt; St&ouml;tten, in St&ouml;tten Richtung Auerberg &uuml;ber Buchen, Reinharten, Prachtsried bis G&uuml;nther. In G&uuml;nther vor Kapelle links ca. 2 Km noch bis Badwerk.
