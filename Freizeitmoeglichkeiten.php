<p><strong>Bei uns ist immer etwas los!</strong>
</p>
</p>
Wenn die Pferde Pause haben, kümmern sich unsere Helfer und wir rund um die Uhr um`s Programm, z.B. Bachwanderung, Ballspiele, Schnitzeljagd oder ein Besuch im Kuhstall...
Wir besuchen die Esel oder das Kelten und Allemannen-Lager. Indianer gibts auch in der Nähe.
</p>
<p>
Langeweile bei schlechtem Wetter kennen wir nicht. In unserer geräumigen Sattelkammer und Spielscheune basteln wir Heutiere oder werkeln mit Salzteig. Unsere Holzpferde sind auch sehr geduldig. Wer`s spannender mag, kann sich im Bogenschießen versuchen. Abends werden dann auch mal am Lagerfeuer Stockbrot und Würstchen verzehrt und tolle Geschichten erzählt. Eine Nachtwanderung gehört auch dazu, da kann man wunderbar die Sterne betrachten. Wer mag darf bei uns auch ( unter Aufsicht ) im Heu übernachten.
</p>
